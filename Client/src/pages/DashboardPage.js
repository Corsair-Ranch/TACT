  //   const totalAirFare =
  //     obj.perDiem.airFare.comAirFare.occupancy *
  //     obj.perDiem.airFare.comAirFare.rate;

  //   const totalLodging =
  //     obj.perDiem.lodging.comLodgingInfo.ratePerOccupancy *
